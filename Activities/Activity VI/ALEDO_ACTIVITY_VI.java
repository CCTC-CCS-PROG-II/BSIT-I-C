import java.util.Scanner;
public class ALEDO_ACTIVITY_VI {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.print("Enter the number of seconds: ");
        int secs = sc.nextInt();


        
        while 
             (secs >= 0) {
                System.out.println(secs);
                secs-=1;
            }
        
        
         
    }
}
  
